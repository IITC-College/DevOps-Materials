#!/usr/bin/env python3
# Dictionaries with different value types

data = {
    "string": "Hello",
    "integer": 42,
    "float": 3.14,
    "boolean": True,
    "list": [1, 2, 3],
    "dict": {"nested": "value"},
    "none": None
}

print(f"String: {data['string']}")
print(f"Integer: {data['integer']}")
print(f"Float: {data['float']}")
print(f"Boolean: {data['boolean']}")
print(f"List: {data['list']}")
print(f"Dict: {data['dict']}")
print(f"None: {data['none']}")
