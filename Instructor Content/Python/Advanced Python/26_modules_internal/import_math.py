#!/usr/bin/env python3
# Using math module

import math

# Mathematical constants
print(f"Pi: {math.pi}")
print(f"E: {math.e}")

# Rounding functions
x = 4.7
print(f"Floor of {x}: {math.floor(x)}")
print(f"Ceil of {x}: {math.ceil(x)}")

# Power and square root
print(f"2^8: {math.pow(2, 8)}")
print(f"Square root of 16: {math.sqrt(16)}")

# Trigonometric functions
angle = math.pi / 4  # 45 degrees
print(f"Sin(45°): {math.sin(angle):.4f}")
print(f"Cos(45°): {math.cos(angle):.4f}")

# Logarithms
print(f"Log(100): {math.log10(100)}")
print(f"Natural log(e): {math.log(math.e)}")
